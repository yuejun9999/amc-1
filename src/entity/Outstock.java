package entity;

public class Outstock {
	private String number;
	private String outstockid;
	private String orderid;
	private String userid;
	private String username;
	private String outstocktime;
	private String outstockstate;
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getOutstockid() {
		return outstockid;
	}
	public void setOutstockid(String outstockid) {
		this.outstockid = outstockid;
	}
	public String getOrderid() {
		return orderid;
	}
	public void setOrderid(String orderid) {
		this.orderid = orderid;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getOutstocktime() {
		return outstocktime;
	}
	public void setOutstocktime(String outstocktime) {
		this.outstocktime = outstocktime;
	}
	public String getOutstockstate() {
		return outstockstate;
	}
	public void setOutstockstate(String outstockstate) {
		this.outstockstate = outstockstate;
	}
	
    
}
