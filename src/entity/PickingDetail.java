package entity;

public class PickingDetail {
	private String ids;
	private String productid;
	private String productname;
	private String productsize;
	private String suppliercode;
	private String pickingdetailnum;
	public String getIds() {
		return ids;
	}
	public void setIds(String ids) {
		this.ids = ids;
	}
	public String getProductid() {
		return productid;
	}
	public void setProductid(String productid) {
		this.productid = productid;
	}
	public String getProductname() {
		return productname;
	}
	public void setProductname(String productname) {
		this.productname = productname;
	}
	public String getProductsize() {
		return productsize;
	}
	public void setProductsize(String productsize) {
		this.productsize = productsize;
	}
	public String getSuppliercode() {
		return suppliercode;
	}
	public void setSuppliercode(String suppliercode) {
		this.suppliercode = suppliercode;
	}
	public String getPickingdetailnum() {
		return pickingdetailnum;
	}
	public void setPickingdetailnum(String pickingdetailnum) {
		this.pickingdetailnum = pickingdetailnum;
	}
	

}
