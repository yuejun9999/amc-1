package entity;

public class OutstockDetail {
	private String number;
	private String outstockdetailid;
	private String outstockid;
	private String productid;
	private String productname;
	private String outstocknum;
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getOutstockdetailid() {
		return outstockdetailid;
	}
	public void setOutstockdetailid(String outstockdetailid) {
		this.outstockdetailid = outstockdetailid;
	}
	public String getOutstockid() {
		return outstockid;
	}
	public void setOutstockid(String outstockid) {
		this.outstockid = outstockid;
	}
	public String getProductid() {
		return productid;
	}
	public void setProductid(String productid) {
		this.productid = productid;
	}
	public String getProductname() {
		return productname;
	}
	public void setProductname(String productname) {
		this.productname = productname;
	}
	public String getOutstocknum() {
		return outstocknum;
	}
	public void setOutstocknum(String outstocknum) {
		this.outstocknum = outstocknum;
	}
	
	

}
